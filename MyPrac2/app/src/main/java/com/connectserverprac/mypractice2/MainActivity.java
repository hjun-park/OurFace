    public void connectServer(View v){ // connect to server 버튼 눌렸을 때
        String postUrl= "http://117.16.44.14:8080/";

        ByteArrayOutputStream stream = new ByteArrayOutputStream();
        BitmapFactory.Options options = new BitmapFactory.Options();
        options.inPreferredConfig = Bitmap.Config.RGB_565; // 이미지파일이 디코딩 될 포맷 지정
        // Read BitMap by file path
        Bitmap bitmap = BitmapFactory.decodeFile(selectedImagePath, options);
        bitmap.compress(Bitmap.CompressFormat.JPEG, 100, stream);
        byte[] byteArray = stream.toByteArray();

        RequestBody postBodyImage = new MultipartBody.Builder()
                .setType(MultipartBody.FORM)
                .addFormDataPart("image", "androidFlask.jpg", RequestBody.create(MediaType.parse("image/*jpg"), byteArray))
                .build();

        TextView responseText = findViewById(R.id.responseText);
        responseText.setText("Please wait ...");

        postRequest(postUrl, postBodyImage);
    }

    void postRequest(String postUrl, RequestBody postBody) {

        OkHttpClient client = new OkHttpClient();

        Request request = new Request.Builder()
                .url(postUrl)
                .post(postBody)
                .build();

        client.newCall(request).enqueue(new Callback() {
            @Override
            public void onFailure(Call call, IOException e) {
                // Cancel the post on failure.
                call.cancel();

                // In order to access the TextView inside the UI thread, the code is executed inside runOnUiThread()
                runOnUiThread(new Runnable() {
                    @Override
                    public void run() {
                        TextView responseText = findViewById(R.id.responseText);
                        responseText.setText("Failed to Connect to Server");
                    }
                });
            }

            @Override
            public void onResponse(Call call, final Response response) throws IOException {
                // In order to access the TextView inside the UI thread, the code is executed inside runOnUiThread()
                runOnUiThread(new Runnable() {
                    @Override
                    public void run() {
                        TextView responseText = findViewById(R.id.responseText);
                        try {
                            responseText.setText(response.body().string());

                            Thread uThread = new Thread() {//네트워크 관련 작업할때 별도의 스레드 생성
                                @Override
                                public void run(){
                                    try{
                                        //서버에 올려둔 이미지 URL
                                        URL url = new URL("http://117.16.44.14:8080/static/116_2.png");
                                        //Web에서 이미지 가져온 후 ImageView에 지정할 Bitmap 만들기

                                        /* URLConnection 생성자가 protected로 선언되어 있으므로
                                        개발자가 직접 HttpURLConnection 객체 생성 불가 */

                                        HttpURLConnection conn = (HttpURLConnection)url.openConnection();
                                        /* openConnection()메서드가 리턴하는 urlConnection 객체는
                                        HttpURLConnection의 인스턴스가 될 수 있으므로 캐스팅해서 사용한다*/

                                        conn.setDoInput(true); //Server 통신에서 입력 가능한 상태로 만듦
                                        conn.connect(); //연결된 곳에 접속할 때 (connect() 호출해야 실제 통신 가능함)

                                        InputStream is = conn.getInputStream(); //inputStream 값 가져오기
                                        bitmap = BitmapFactory.decodeStream(is); // Bitmap으로 반환

                                    }catch (MalformedURLException e){
                                        e.printStackTrace();

                                    }catch (IOException e){
                                        e.printStackTrace();
                                    }
                                }
                            };

                            uThread.start(); // 작업 Thread 실행

                            try{
                                //메인 Thread는 별도의 작업을 완료할 때까지 대기한다!
                                //join() 호출하여 별도의 작업 Thread가 종료될 때까지 메인 Thread가 기다림
                                //join() 메서드는 InterruptedException을 발생시킨다.
                                uThread.join();

                                //작업 Thread에서 이미지를 불러오는 작업을 완료한 뒤
                                //UI 작업을 할 수 있는 메인 Thread에서 ImageView에 이미지 지정
                                imageView.setImageBitmap(bitmap);

                            }catch (InterruptedException e){
                                e.printStackTrace();
                            }
                        } catch (IOException e) {
                            e.printStackTrace();
                        }
                    }
                });
            }
        });
    }

